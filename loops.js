//Loops
//The For Loop

/*Create a program that loops from 5 to 10 and logs each number to the console.*/
for (let number = 5; number <= 10; number++)
  {
    console.log(number);
  }
  

//Looping in Reverse
for (let counter = 3; counter >= 0; counter--){
  console.log(counter);
}
